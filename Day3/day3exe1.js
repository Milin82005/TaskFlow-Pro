function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log("Addition:", add(14, 20));
console.log("Subtraction:", subtract(30, 5));
console.log("Multiplication:", multiply(5, 5));
console.log("Division:", divide(50, 2));